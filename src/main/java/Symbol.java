public enum Symbol {
    SYMBOL_DIEZ{
        @Override
        public String toString(){
            return "#";
        }
    },
    SYMBOL_EXCLAMATION{
        @Override
        public String toString(){
            return "!";
        }
    },
    SYMBOL_QUESTION{
        @Override
        public String toString(){
            return "?";
        }
    },
    SYMBOL_AND{
        @Override
        public String toString(){
            return "&";
        }
    },
    SYMBOL_AROND{
        @Override
        public String toString(){
            return "@";
        }
    },
    SYMBOL_MODULO{
        @Override
        public String toString(){
            return "%";
        }
    },
    SYMBOL_STAR{
        @Override
        public String toString(){
            return "*";
        }
    },
    SYMBOL_DOLLAR{
        @Override
        public String toString(){
            return "$";
        }
    };



}
